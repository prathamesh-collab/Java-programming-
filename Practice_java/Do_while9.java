// Do While loop :- the loop will be executed at least once even the condition is false 
        // because code block executed before the condition 
public class Do_while9 {
    public static void main(String[] args ){
        int i = 0;
        int n = 5;
        do {
            System.out.println(n*i);
            i++;

        }while(i<=10);
    }
    
}