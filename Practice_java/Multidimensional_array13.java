//multidimensional array :- 
public class Multidimensional_array13 {
    public static void main(String[] args ){
        int[][] num = { {1,2,3} , {5,9,8} };
        for(int i = 0; i<num.length ; i++){
            for(int j = 0 ; j<num.length;j++){
                System.out.println(num[i][j]);
            }
        }

    }
    
}